from .schemas import Artifact, Source

_ARTIFACT_ID = re.compile(r"^[0-9a-f]{32}$")


class ReportService:
    def __init__(self, artifact_dir: Path, max_characters: int = 100_000):
        self.artifact_dir = artifact_dir
        self.max_characters = max_characters

    def prepare(self) -> None:
        self.artifact_dir.mkdir(parents=True, exist_ok=True)

    def create(
        self,
        *,
        owner_namespace: str,
        title: str,
        markdown: str,
        sources: Iterable[Source] = (),
    ) -> Artifact:
        if not title.strip() or len(title) > 200:
            raise ValueError("report title is invalid")
        if not markdown.strip() or len(markdown) > self.max_characters:
            raise ValueError("report content is invalid or too large")

        self.prepare()
        artifact_id = uuid4().hex
        final_path = self._path(owner_namespace, artifact_id)
        temporary_path: Path | None = None
        try:
            with tempfile.NamedTemporaryFile(
                prefix="report-", suffix=".pdf.tmp", dir=self.artifact_dir, delete=False
            ) as temporary:
                temporary_path = Path(temporary.name)
            self._render_pdf(temporary_path, title.strip(), markdown.strip(), tuple(sources))
            os.replace(temporary_path, final_path)
        finally:
            if temporary_path is not None and temporary_path.exists():
                temporary_path.unlink(missing_ok=True)

        return Artifact(
            id=artifact_id,
            title=title.strip(),
            size_bytes=final_path.stat().st_size,
            download_url=f"/v1/artifacts/{artifact_id}",
            created_at=datetime.now(UTC),
        )

    def find(self, owner_namespace: str, artifact_id: str) -> Path | None:
        if _ARTIFACT_ID.fullmatch(artifact_id) is None:
            return None
        path = self._path(owner_namespace, artifact_id)
        return path if path.is_file() else None

    def _path(self, owner_namespace: str, artifact_id: str) -> Path:
        return self.artifact_dir / f"{owner_namespace}_{artifact_id}.pdf"

    def _render_pdf(
        self,
        output_path: Path,
        title: str,
        markdown: str,
        sources: tuple[Source, ...],
    ) -> None:
        document = SimpleDocTemplate(
            str(output_path),
            pagesize=A4,
            title=title,
            author="MicroManus",
            subject="Deep research report",
            leftMargin=20 * mm,
            rightMargin=20 * mm,
            topMargin=24 * mm,
            bottomMargin=20 * mm,
        )
        styles = self._styles()
        story: list[object] = [
            Paragraph(escape(title), styles["ReportTitle"]),
            Spacer(1, 5 * mm),
            Paragraph(
                f"Generated by MicroManus · {datetime.now(UTC).strftime('%d %B %Y, %H:%M UTC')}",
                styles["Meta"],
            ),
            Spacer(1, 4 * mm),
            HRFlowable(width="100%", thickness=0.6, color=colors.HexColor("#CBD5E1")),
            Spacer(1, 5 * mm),
        ]
        story.extend(self._markdown_story(markdown, styles))

        unique_sources = self._unique_sources(sources)
        if unique_sources:
            story.extend([PageBreak(), Paragraph("Sources", styles["Heading1"])])
            for index, source in enumerate(unique_sources, start=1):
                url = escape(str(source.url), quote=True)
                label = escape(source.title or str(source.url))
                snippet = escape(source.snippet)
                story.append(
                    Paragraph(
                        f'<b>{index}.</b> <a href="{url}" color="#2563EB">{label}</a>',
                        styles["BodyText"],
                    )
                )
                if snippet:
                    story.append(Paragraph(snippet, styles["SourceSnippet"]))
                story.append(Spacer(1, 2.5 * mm))

        def draw_page(canvas: Canvas, doc: SimpleDocTemplate) -> None:
            canvas.saveState()
            canvas.setStrokeColor(colors.HexColor("#E2E8F0"))
            canvas.line(20 * mm, 14 * mm, A4[0] - 20 * mm, 14 * mm)
            canvas.setFillColor(colors.HexColor("#64748B"))
            canvas.setFont("Helvetica", 8)
            canvas.drawString(20 * mm, 9 * mm, "MicroManus research artifact")
            canvas.drawRightString(A4[0] - 20 * mm, 9 * mm, f"Page {doc.page}")
            canvas.restoreState()

        document.build(story, onFirstPage=draw_page, onLaterPages=draw_page)

