    def _markdown_story(self, markdown: str, styles: dict[str, ParagraphStyle]) -> list[object]:
        story: list[object] = []
        paragraph_lines: list[str] = []
        code_lines: list[str] = []
        in_code = False

        def flush_paragraph() -> None:
            if paragraph_lines:
                text = " ".join(line.strip() for line in paragraph_lines)
                story.append(Paragraph(escape(text), styles["BodyText"]))
                paragraph_lines.clear()

        for raw_line in markdown.splitlines():
            line = raw_line.rstrip()
            if line.strip().startswith("```"):
                flush_paragraph()
                if in_code:
                    story.append(Preformatted("\n".join(code_lines), styles["Code"]))
                    code_lines.clear()
                in_code = not in_code
                continue
            if in_code:
                code_lines.append(line)
                continue
            if not line.strip():
                flush_paragraph()
                continue
            heading = re.match(r"^(#{1,3})\s+(.+)$", line)
            if heading:
                flush_paragraph()
                story.append(
                    Paragraph(escape(heading.group(2)), styles[f"Heading{len(heading.group(1))}"])
                )
                continue
            bullet = re.match(r"^\s*[-*+]\s+(.+)$", line)
            numbered = re.match(r"^\s*(\d+)[.)]\s+(.+)$", line)
            if bullet or numbered:
                flush_paragraph()
                marker = "•" if bullet else f"{numbered.group(1)}."
                text = bullet.group(1) if bullet else numbered.group(2)
                story.append(Paragraph(f"{marker} {escape(text)}", styles["Bullet"]))
                continue
            if line.lstrip().startswith(">"):
                flush_paragraph()
                story.append(Paragraph(escape(line.lstrip()[1:].strip()), styles["Quote"]))
                continue
            if re.match(r"^[-*_]{3,}$", line.strip()):
                flush_paragraph()
                story.append(
                    HRFlowable(width="100%", thickness=0.5, color=colors.HexColor("#CBD5E1"))
                )
                story.append(Spacer(1, 2 * mm))
                continue
            paragraph_lines.append(line)

        flush_paragraph()
        if code_lines:
            story.append(Preformatted("\n".join(code_lines), styles["Code"]))
        return story

    @staticmethod
    def _unique_sources(sources: tuple[Source, ...]) -> tuple[Source, ...]:
        seen: set[str] = set()
        result: list[Source] = []
        for source in sources:
            url = str(source.url)
            if url not in seen:
                seen.add(url)
                result.append(source)
        return tuple(result)
